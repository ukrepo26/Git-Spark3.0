// this is method overloading where method name same but
// method signature different not consider retun type 
package com.example.OverloadingDemo;
public class OverloadingDemo {
    
    public int sum(int a)
    {
       return a;    
    }
    
    public int sum(int a,int b)
    {
       return a+b;

    }
    
    public String sum(int a,int b,int c)
    {
       return "umesh";    
    }
}
